class Movie{
	constructor(title, year, description, image){
		this.title = title;
		this.year = year;
		this.description = description;
		this.image = image;
	}

	setTitle(title){
		this.title = title;
	}

	setYear(year) {
		this.year = year;
	}

	setDescription(description) {
		this.description = description;
	}

	setImage(image) {
		this.image = image;
	}
}